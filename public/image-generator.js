function submissionValidationFailure(source, message, options = {}) {
  if (source === 'auto' && !options.wait) {
    stopAutoGenerate(message, false);
  } else if (source === LLM_AUTO_GENERATION_SOURCE && !options.wait) {
    stopLlmAutoGenerate(message, false, { error: true });
  } else {
    setStatus(message, false);
  }
  return false;
}

function submitGenerationRequest(source = 'manual', options = {}) {
  if (!isAutomaticSubmissionSource(source)) {
    if (state.autoGenerateEnabled) setAutoGenerateEnabled(false, { message: 'Auto-Generate turned off for manual generation.', ok: true });
    if (state.llmAutoGenerateEnabled) stopLlmAutoGenerate('LLM Auto-Generate turned off for manual generation.', true);
  }
  persistImageLabFormValues();
  if (state.prewarmingModel) {
    return submissionValidationFailure(source, 'Model prewarming is still running. Submit again once the checkpoint is ready.', { wait: true });
  }
  if (!reportGenerationFormValidity()) {
    return submissionValidationFailure(source, 'Complete the required generation fields before generating.');
  }

  const payload = clonePayload(buildGenerationPayload(options));
  const selectedSource = selectedGenerationSource();
  if (!selectedSource || !payload.generation_source_id) {
    return submissionValidationFailure(source, 'Choose a valid generation source before generating.');
  }
  if (selectedSource.type === 'checkpoint' && !payload.model) {
    return submissionValidationFailure(source, 'Choose a checkpoint generation source before generating.');
  }
  if (!payload.prompt) {
    return submissionValidationFailure(source, 'Positive prompt is required.');
  }

  const pendingJob = createPendingJob(payload, { source, llmPrompt: options.llmPrompt });
  if (source === 'auto') markJobAsAutoGenerated(pendingJob);
  if (source === LLM_AUTO_GENERATION_SOURCE) markJobAsLlmAutoGenerated(pendingJob);
  addPendingJob(pendingJob);
  const prefix = source === 'auto'
    ? 'Auto-Generate queued'
    : source === LLM_AUTO_GENERATION_SOURCE
      ? 'LLM Auto-Generate queued'
      : 'Queued generation request';
  setStatus(`${prefix} ${pendingJob.clientSequence}. A pending gallery card was added.`);
  void submitGenerationJob(pendingJob.clientId, payload, { source });
  renderControlChrome();
  return true;
}


