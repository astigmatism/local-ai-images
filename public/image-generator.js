async function startNextLlmAutoPromptRequest() {
  if (!state.llmAutoGenerateEnabled || state.llmAutoGeneratePromptInFlight || state.llmAutoGeneratePreparedPrompt) return false;
  const guidance = currentLlmGuidanceValue();
  if (!guidance) {
    setLlmPromptStatus('Enter guidance before starting LLM Auto-Generate.', false);
    stopLlmAutoGenerate('LLM Auto-Generate stopped: enter LLM image prompt guidance before starting.', false, { error: true });
    return false;
  }

  const requestId = state.llmAutoGenerateRequestId + 1;
  state.llmAutoGenerateRequestId = requestId;
  state.llmAutoGeneratePromptInFlight = { requestId, guidance, startedAt: new Date().toISOString() };
  setLlmAutoGenerateStatus('sending-llm', 'Sending guidance');
  setLlmPromptStatus('LLM Auto-Generate is building the next positive prompt...');
  renderControlChrome();

  try {
    const result = await buildImagePromptFromGuidance(guidance);
    if (!state.llmAutoGenerateEnabled || requestId !== state.llmAutoGenerateRequestId) return false;
    const prompt = String(result?.prompt || '').trim();
    if (!prompt) throw new Error('The local LLM returned an empty prompt.');

    const preparedPrompt = {
      prompt,
      guidance,
      createdAt: new Date().toISOString(),
      elapsedMs: Number.isFinite(Number(result?.elapsedMs)) ? Number(result.elapsedMs) : null,
      modelInfo: isPlainObject(result?.modelInfo) ? clonePayload(result.modelInfo) : result?.modelInfo || null
    };
    state.llmAutoGeneratePromptInFlight = null;
    state.llmAutoGeneratePreparedPrompt = preparedPrompt;
    setPositivePromptValue(prompt);
    const elapsed = Number.isFinite(Number(preparedPrompt.elapsedMs)) ? ` in ${Math.round(Number(preparedPrompt.elapsedMs))} ms` : '';
    setLlmPromptStatus(`LLM Auto-Generate prepared the next positive prompt${elapsed}.`);
    setLlmAutoGenerateStatus('prepared', 'Prompt ready');
    queueLlmAutoGenerationCheck();
    return true;
  } catch (error) {
    if (requestId === state.llmAutoGenerateRequestId) {
      state.llmAutoGeneratePromptInFlight = null;
      state.llmAutoGeneratePreparedPrompt = null;
      const message = error?.message || 'Local LLM prompt request failed.';
      setLlmPromptStatus(message, false);
      stopLlmAutoGenerate(`LLM Auto-Generate stopped because prompt generation failed: ${message}`, false, { error: true });
    }
    return false;
  } finally {
    if (requestId === state.llmAutoGenerateRequestId) {
      renderControlChrome();
      refreshImageLabSlideshowStatus();
    }
  }
}

async function maybeRunLlmAutoGeneration() {
  if (!state.llmAutoGenerateEnabled || state.llmAutoGenerateSubmitting || state.favoriteLoadAutomaticGenerationGuard) return;
  if (!state.imageJobs || state.prewarmingModel) {
    setLlmAutoGenerateStatus('waiting-jobs', llmAutoGenerateStatusText('waiting-jobs'));
    scheduleLlmAutoGenerateWaitCheck();
    return;
  }

  const activeCount = activeGenerationJobCount();
  if (activeCount > 0) {
    if (state.llmAutoGeneratePreparedPrompt) setLlmAutoGenerateStatus('waiting-image', 'Waiting for image');
    else if (state.llmAutoGeneratePromptInFlight) setLlmAutoGenerateStatus('waiting-next-prompt', 'Waiting for next prompt');
    else void startNextLlmAutoPromptRequest();
    scheduleLlmAutoGenerateWaitCheck();
    return;
  }

  if (!state.llmAutoGeneratePreparedPrompt) {
    if (state.llmAutoGeneratePromptInFlight) {
      setLlmAutoGenerateStatus('waiting-llm', 'Waiting for LLM');
      scheduleLlmAutoGenerateWaitCheck();
      return;
    }
    void startNextLlmAutoPromptRequest();
    return;
  }

  const preparedPrompt = state.llmAutoGeneratePreparedPrompt;
  state.llmAutoGeneratePreparedPrompt = null;
  state.llmAutoGenerateSubmitting = true;
  setPositivePromptValue(preparedPrompt.prompt);
  setLlmAutoGenerateStatus('generating', 'Generating image');
  renderControlChrome();
  try {
    const submitted = submitGenerationRequest(LLM_AUTO_GENERATION_SOURCE, {
      promptOverride: preparedPrompt.prompt,
      llmGuidanceOverride: preparedPrompt.guidance,
      llmPrompt: preparedPrompt
    });
    if (submitted && state.llmAutoGenerateEnabled) void startNextLlmAutoPromptRequest();
    else if (state.llmAutoGenerateEnabled && !state.llmAutoGeneratePromptInFlight && !state.llmAutoGeneratePreparedPrompt) queueLlmAutoGenerationCheck();
  } finally {
    state.llmAutoGenerateSubmitting = false;
    renderControlChrome();
  }
}

