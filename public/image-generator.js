async function handleFavoriteClick(event) {
  const button = event.target.closest('[data-favorite-action]');
  if (!button || button.disabled) return;
  const card = button.closest('[data-favorite-id]');
  const favoriteId = card?.dataset.favoriteId;
  if (!favoriteId) return;
  const summary = favoriteById(favoriteId);
  const action = button.dataset.favoriteAction;
  try {
    if (action === 'load') {
      state.favoriteLoadAutomaticGenerationGuard = true;
      try {
        const response = await fetchJson(`/api/v1/image-favorites/${encodeURIComponent(favoriteId)}`);
        const favorite = response.favorite;
        const warnings = applyGenerationPayloadToControls(favorite?.requestPayload || {});
        if (selectedSourceRequiresPrewarm() && warnings.every((warning) => !warning.includes('generation source') && !warning.startsWith('model '))) {
          await prewarmSelectedModel({ preserveFormValues: true });
        }
        const warningText = warnings.length ? ` Restored what I could; check: ${warnings.join(', ')}.` : '';
        setStatus(`Loaded favorite "${favorite?.title || summary?.title || favoriteId}" into controls. It was not submitted.${warningText}`, warnings.length === 0);
        closeImageFavoritesModal();
      } finally {
        releaseFavoriteLoadAutomaticGenerationGuard();
      }
      return;
    }
    if (action === 'delete') {
      const ok = window.confirm(`Delete image favorite "${summary?.title || favoriteId}"?`);
      if (!ok) return;
      await fetchJson(`/api/v1/image-favorites/${encodeURIComponent(favoriteId)}`, { method: 'DELETE' });
      await refreshFavoritesOnly('Deleted image favorite.');
    }
  } catch (error) {
    setStatus(error.message, false);
  }
}

function initResizableControls() {
  const handle = $('#image-lab-controls-resize');
  const controls = $('.image-lab-controls');
  if (!handle || !controls) return;
  applyControlsHeight();

  let dragStartY = 0;
  let dragStartHeight = 0;
  let isDragging = false;

  const finishDrag = (event) => {
    if (!isDragging) return;
    isDragging = false;
    document.body.classList.remove('image-lab-resizing');
    try {
      handle.releasePointerCapture(event.pointerId);
    } catch {
      // Pointer capture may already be released if the drag ended outside the handle.
    }
    applyControlsHeight(state.controlsHeight, true);
  };

  handle.addEventListener('pointerdown', (event) => {
    if (event.button !== 0) return;
    event.preventDefault();
    isDragging = true;
    dragStartY = event.clientY;
    dragStartHeight = normalizedControlsHeight();
    document.body.classList.add('image-lab-resizing');
    handle.setPointerCapture(event.pointerId);
  });

  handle.addEventListener('pointermove', (event) => {
    if (!isDragging) return;
    event.preventDefault();
    applyControlsHeight(dragStartHeight + event.clientY - dragStartY);
  });

  handle.addEventListener('pointerup', finishDrag);
  handle.addEventListener('pointercancel', finishDrag);
  handle.addEventListener('lostpointercapture', () => {
    if (!isDragging) return;
    isDragging = false;
    document.body.classList.remove('image-lab-resizing');
    applyControlsHeight(state.controlsHeight, true);
  });

  handle.addEventListener('keydown', (event) => {
    const step = event.shiftKey ? 40 : 16;
    let nextHeight = null;
    if (event.key === 'ArrowUp') nextHeight = normalizedControlsHeight() - step;
    if (event.key === 'ArrowDown') nextHeight = normalizedControlsHeight() + step;
    if (event.key === 'PageUp') nextHeight = normalizedControlsHeight() - 80;
    if (event.key === 'PageDown') nextHeight = normalizedControlsHeight() + 80;
    if (event.key === 'Home') nextHeight = controlsHeightBounds().min;
    if (event.key === 'End') nextHeight = controlsHeightBounds().max;
    if (nextHeight === null) return;
    event.preventDefault();
    applyControlsHeight(nextHeight, true);
  });

  window.addEventListener('resize', () => applyControlsHeight());
}

function initResizableControlColumns() {
  const handles = controlsColumnHandles();
  if (handles.length === 0) return;
  applyControlsColumnLayout();

  handles.forEach((handle) => {
    let dragStartX = 0;
    let dragStartWidths = null;
    let isDragging = false;
    const handleIndex = controlsColumnResizeIndex(handle);

    const finishDrag = (event) => {
      if (!isDragging) return;
      isDragging = false;
      document.body.classList.remove('image-lab-column-resizing');
      try {
        handle.releasePointerCapture(event.pointerId);
      } catch {
        // Pointer capture may already be released if the drag ended outside the handle.
      }
      applyControlsColumnLayout(true);
    };

    handle.addEventListener('pointerdown', (event) => {
      if (event.button !== 0 || controlsColumnsAreStacked()) return;
      event.preventDefault();
      isDragging = true;
      dragStartX = event.clientX;
      dragStartWidths = controlsColumnWidthsFromRatios();
      document.body.classList.add('image-lab-column-resizing');
      handle.setPointerCapture(event.pointerId);
    });

    handle.addEventListener('pointermove', (event) => {
      if (!isDragging || !dragStartWidths) return;
      event.preventDefault();
      resizeControlsColumnsFromStart(dragStartWidths, handleIndex, event.clientX - dragStartX);
    });

    handle.addEventListener('pointerup', finishDrag);
    handle.addEventListener('pointercancel', finishDrag);
    handle.addEventListener('lostpointercapture', () => {
      if (!isDragging) return;
      isDragging = false;
      document.body.classList.remove('image-lab-column-resizing');
      applyControlsColumnLayout(true);
    });

    handle.addEventListener('keydown', (event) => {
      const step = event.shiftKey ? 48 : 24;
      let delta = null;
      if (event.key === 'ArrowLeft') delta = -step;
      if (event.key === 'ArrowRight') delta = step;
      if (event.key === 'Home') delta = -10000;
      if (event.key === 'End') delta = 10000;
      if (delta === null || controlsColumnsAreStacked()) return;
      event.preventDefault();
      nudgeControlsColumns(handleIndex, delta, true);
    });
  });

  window.addEventListener('resize', () => applyControlsColumnLayout());
}


function handleGenerationSourcePickerClick(event) {
  const categoryHeader = event.target.closest?.('[data-source-category-toggle-key]');
  if (categoryHeader) {
    event.preventDefault();
    event.stopPropagation();
    toggleGenerationSourceCategoryGroup(categoryHeader.dataset.sourceCategoryToggleKey || '');
    return;
  }

  const favoriteButton = event.target.closest?.('[data-source-favorite-id]');
  if (favoriteButton) {
    event.preventDefault();
    event.stopPropagation();
    void toggleGenerationSourceFavorite(favoriteButton.dataset.sourceFavoriteId || '');
    return;
  }

  const ratingStar = event.target.closest?.('[data-source-rating-id]');
  if (ratingStar) {
    event.preventDefault();
    event.stopPropagation();
    requestGenerationSourceRating(
      ratingStar.dataset.sourceRatingId || '',
      Number(ratingStar.dataset.sourceRatingValue || 0),
      { toggleFirstStar: true }
    );
    return;
  }

  const ratingClear = event.target.closest?.('[data-source-rating-clear-id]');
  if (ratingClear) {
    event.preventDefault();
    event.stopPropagation();
    requestGenerationSourceRating(ratingClear.dataset.sourceRatingClearId || '', 0);
    return;
  }

  const notesToggle = event.target.closest?.('[data-source-notes-toggle-id]');
  if (notesToggle) {
    event.preventDefault();
    event.stopPropagation();
    toggleGenerationSourceNotes(notesToggle.dataset.sourceNotesToggleId || '');
    return;
  }

  const notesSave = event.target.closest?.('[data-source-notes-save-id]');
  if (notesSave) {
    event.preventDefault();
    event.stopPropagation();
    void saveGenerationSourceNotes(notesSave.dataset.sourceNotesSaveId || '');
    return;
  }

  const notesCancel = event.target.closest?.('[data-source-notes-cancel-id]');
  if (notesCancel) {
    event.preventDefault();
    event.stopPropagation();
    cancelGenerationSourceNotesEdit(notesCancel.dataset.sourceNotesCancelId || '');
    return;
  }

  if (event.target.closest?.(
    'textarea, input, button, .image-lab-source-favorite-cell, .image-lab-source-rating-cell, .image-lab-source-category-field, .image-lab-source-row-actions, .image-lab-source-notes-panel'
  )) {
    event.stopPropagation();
    return;
  }

  const row = event.target.closest?.('[data-source-id]');
  if (!row) return;
  event.preventDefault();
  selectGenerationSourceFromPicker(row.dataset.sourceId || '');
}

