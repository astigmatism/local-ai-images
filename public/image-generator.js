function renderImageLabSlideshowSlide() {
  if (!imageSlideshow.isOpen) return;
  applyImageLabSlideshowFitMode();
  const slide = imageSlideshow.currentSlide;
  if (!slide) {
    if (imageSlideshow.image) {
      imageSlideshow.image.hidden = true;
      imageSlideshow.image.removeAttribute('src');
      delete imageSlideshow.image.dataset.slideshowSrc;
    }
    refreshImageLabSlideshowStatus();
    return;
  }

  refreshImageLabSlideshowStatus();
  if (!imageSlideshow.image) return;
  const image = imageSlideshow.image;
  image.alt = slide.alt;
  if (image.dataset.slideshowSrc === slide.src) {
    if (image.complete && image.naturalWidth > 0) image.hidden = false;
    if (!image.hidden && imageSlideshow.message) imageSlideshow.message.hidden = true;
    return;
  }

  image.hidden = true;
  image.dataset.slideshowSrc = slide.src;
  setImageLabSlideshowMessage('Loading next slide...');
  image.onload = () => {
    if (image.dataset.slideshowSrc !== slide.src) return;
    image.hidden = false;
    imageSlideshow.overlay?.classList.remove('has-error');
    if (imageSlideshow.message) imageSlideshow.message.hidden = true;
    refreshImageLabSlideshowStatus();
  };
  image.onerror = () => {
    if (image.dataset.slideshowSrc !== slide.src) return;
    image.hidden = true;
    setImageLabSlideshowMessage('Unable to load this generated image. Waiting for the next completed slide.', true);
    refreshImageLabSlideshowStatus('Image load failed. Waiting for the next completed slide.');
  };
  image.src = slide.src;
}

